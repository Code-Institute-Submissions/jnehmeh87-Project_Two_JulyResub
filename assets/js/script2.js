function shuffle(vals) {
    let currentIndex = vals.length, randomIndex;
    while (currentIndex != 0) {
        randomIndex = Math.floor(Math.random() * currentIndex);
        currentIndex--;
        [vals[currentIndex], vals[randomIndex]] = [
            vals[randomIndex], vals[currentIndex]];
    }
    return vals;
}

